package dto;


public class fb_CouponDTO {
	
	private int coupon_no, coupon_discount, member_no, couponbook_no;
	private String coupon_name, coupon_state;
	

	public fb_CouponDTO() {

	}


	public int getCoupon_no() {
		return coupon_no;
	}


	public void setCoupon_no(int coupon_no) {
		this.coupon_no = coupon_no;
	}


	public int getCoupon_discount() {
		return coupon_discount;
	}


	public void setCoupon_discount(int coupon_discount) {
		this.coupon_discount = coupon_discount;
	}


	public int getMember_no() {
		return member_no;
	}


	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}


	public int getCouponbook_no() {
		return couponbook_no;
	}


	public void setCouponbook_no(int couponbook_no) {
		this.couponbook_no = couponbook_no;
	}


	public String getCoupon_name() {
		return coupon_name;
	}


	public void setCoupon_name(String coupon_name) {
		this.coupon_name = coupon_name;
	}


	public String getCoupon_state() {
		return coupon_state;
	}


	public void setCoupon_state(String coupon_state) {
		this.coupon_state = coupon_state;
	}
	
	
	
	

}
